// Auto-update Card Editing page on attribute values changed
$(document).ready(function () {
  
  var $questionText = $("textarea#card_question_text");
  var $questionView = $("div.card_front p.card_text");
  
  $questionText.keyup(function () {
    $questionView.html($questionText.val());
  });
  
  var $answerText = $("textarea#card_answer_text");
  var $answerView = $("div.card_back p.card_text");
  
  $answerText.keyup(function () {
    $answerView.html($answerText.val());
  });
  
});